package cn.com.dhcc.creditquery.ent.queryweb.vo;

public class User {
	private String id;
	private String orgId;
	
	public User(){}
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
}
